//games numbers:
// 0 - empty cell
// 1 - filled cell
// 2 - crossed cell

class Game {
  constructor() {}

  getStateOfCell(cellId) {
    let currentRaw = 0;
    const recursion = (cellId) => {
      if (+cellId < this.columnsCount) {
        const currentCell = this.usersBoard[currentRaw][cellId];
        return currentCell;
      } else {
        currentRaw += 1;
        return recursion(cellId - this.columnsCount);
      }
    };
    return recursion(cellId);
  }

  fillInACell(cellId, typeOfClick) {
    const columnIdx = cellId % this.columnsCount;
    const rowIdx = Math.floor(cellId / this.columnsCount);

    const currentCell = this.usersBoard[rowIdx][columnIdx];

    if (typeOfClick === currentCell) {
      return;
    }

    this.usersBoard[rowIdx][columnIdx] = typeOfClick;

    return currentCell;
  }
  checkWin() {
    for (let y = 0; y < this.rowsCount; y += 1) {
      for (let i = 0; i < this.columnsCount; i += 1) {
        if (!(this.solvedBoard[y][i] === this.usersBoard[y][i])) {
          let usersCell = this.usersBoard[y][i];
          const solvedCell = this.solvedBoard[y][i];
          if (usersCell === 2) {
            // if cell is crossed change it to 0 for comparison;
            usersCell = 0;
            if (solvedCell === usersCell) {
              continue;
            }
          }
          return false; // if at least one cell is not equal
        }
      }
    }
    return true; // if all cell are equal
  }
}

export class Nonogram extends Game {
  constructor(
    name,
    solvedBoard,
    usersBoard,
    cluesColumns,
    cluesRows,
    difficulty
  ) {
    super();
    this.name = name;
    this.solvedBoard = solvedBoard;
    this.rowsCount = this.solvedBoard.length;
    this.columnsCount = this.solvedBoard[0].length;
    this.usersBoard = usersBoard;
    this.cluesColumns = cluesColumns;
    this.cluesRows = cluesRows;
    this.difficulty = difficulty;
  }
  getMaxLengthOfCluesColumns() {
    let maxLength = 0;
    for (let i = 0; i < this.cluesColumns.length; i += 1) {
      const currentLength = this.cluesColumns[i].length;
      if (maxLength < currentLength) {
        maxLength = currentLength;
      }
    }
    return maxLength;
  }
  getMaxLengthOfCluesRows() {
    let maxLength = 0;
    for (let i = 0; i < this.cluesRows.length; i += 1) {
      const currentLength = this.cluesRows[i].length;
      if (maxLength < currentLength) {
        maxLength = currentLength;
      }
    }
    return maxLength;
  }
  getDifficulty() {
    return `${this.difficulty}(${this.rowsCount}x${this.columnsCount})`;
  }
}

